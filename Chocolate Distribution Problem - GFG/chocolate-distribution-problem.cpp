class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    sort(a.begin(), a.end());
    
    int i = 0;
    int j = m-1;
    long long mini_diff = INT_MAX;
    while(j < n)
    {
        mini_diff = min(mini_diff, a[j] - a[i]);
        j++;
        i++;
    }
    
    return mini_diff;
    
    }   
};

